# Data Types
# -----------

# String
s = "Hello"
print(s[0])  # Returns 'H'
print(s[4])  # Returns 'o'

s = '123' + '345'
print(s)

# Integer
i = 123 + 345
print(i)

i = 123_456_789  # Use _ in place of ,
print(i)

# Float
f = 3.14159
print(f)

# Boolean
t = True
f = False
print(t, f)
